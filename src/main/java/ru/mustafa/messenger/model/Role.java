package ru.mustafa.messenger.model;

/**
 * Enumeration representing user roles within the system.
 *
 * @author Mustafa
 * @version 1.0.
 */
public enum Role {
    /**
     * Standard user role with basic access privileges.
     */
    ROLE_USER,
    /**
     * Administrator user role with full system control privileges.
     */
    ROLE_ADMIN
}
